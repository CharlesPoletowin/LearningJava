package fourthWeek;

public class Parameter {
	public double a=0;
	public double b=0;
	
	public Parameter (double a1, double b1){
		this.a=a1;
		this.b=b1;
	}
	public double getA() {
        return a;
    }
	public void setA(double a) {
        this.a = a;
    }
	public double getB() {
        return b;
    }
	public void setB(double b) {
        this.b = b;
    }

}
